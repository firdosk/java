package xavier;

public class CubeSquare {

	    public static void main(String[] args) {
	    	
	        int[] numbers = {2, 3, 4};

	        for (int number : numbers) {
	            // Calculate square
	            int square = number * number;
	            
	            System.out.println(square);

	            // Calculate cube
	            int cube = number * number * number;
	            
	            System.out.println(cube);
	        }
	    }
	}


